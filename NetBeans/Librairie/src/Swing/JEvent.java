    private DefaultTableModel initEventTable(){
        
        DefaultTableModel tableModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
               //all cells false
               
               if (column == jTable.getColumnModel().getColumnCount()-1){
                   return true;
               }
               
               return false;
            }
        };
        
        tableModel.addColumn("");
        tableModel.addColumn("Nom");
        tableModel.addColumn("Description");
        tableModel.addColumn("Date de d√©but");
        tableModel.addColumn("Date de fin");
        tableModel.addColumn("Code Promo");
        tableModel.addColumn("");
        
        ImageIcon icon_edit = null;
        ImageIcon icon_add = null;
        try {
            icon_edit = new ImageIcon(getClass().getClassLoader().getResource("resources/images/icon_edit_30.png"));
            icon_add = new ImageIcon(getClass().getClassLoader().getResource("resources/images/icon_add_30.png"));
        } catch (java.lang.NullPointerException ex) {}
        
        Vector vec = new Vector();
        vec.add("");
        vec.add("");
        vec.add("");
        vec.add("");
        vec.add("");
        vec.add("");
        if (icon_add == null) {
            vec.add("Add");
        } else {
            vec.add(icon_add);
        }
        tableModel.addRow(vec);
        
        for (Evenement event : Evenement.getArrayListAllEvenement()){
            vec = new Vector();
            vec.add(event);
            vec.add(event.getNom());
            vec.add(event.getDescription());
            vec.add(event.getDateDebut().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.MEDIUM)));
            if (event.getDateFin() != null){
                vec.add(event.getDateFin().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.MEDIUM)));
            } else {
                vec.add("");
            }
            
            vec.add(event.getCodePromo());
            if (icon_edit == null) {
                vec.add("E");
            } else {
                vec.add(icon_edit);
            }
            
            tableModel.addRow(vec);
        }
        
        return tableModel;
    }
    
    
